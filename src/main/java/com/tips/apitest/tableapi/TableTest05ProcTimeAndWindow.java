public class TableTest05ProcTimeAndWindow {
    public static void main(String[] args) throws Exception{
        StreamExecutionEnvironment env = StreamExecutionEnvironment.getExecutionEnvironment();
        env.setParallelism(1);
        env.setStreamTimeCharacteristic(TimeCharacteristic.EventTime);
        EnvironmentSettings settings = EnvironmentSettings.newInstance().inStreamingMode().useBlinkPlanner().build();
        StreamTableEnvironment tableEnv = StreamTableEnvironment.create(env, settings);

        String inputPath = "./src/main/resources/sensor.txt";
        DataStream<String> inputStream = env.readTextFile(inputPath, "UTF-8");

        DataStream<SensorReading> dataStream = inputStream.map(line -> {
            String[] fields = line.split(",");
            return new SensorReading(fields[0], Long.valueOf(fields[1]), Double.valueOf(fields[2]));
        });

        // tableEnv.createTemporaryView("inputTable", dataStream, "id, timestamp as ts, temperature as temp, pt.proctime ");
        // Table inputTable = tableEnv.from("inputTable");
        Table outputTable = tableEnv.fromDataStream(dataStream, "id, timestamp as ts, temperature as temp, pt.proctime");

        tableEnv.toAppendStream(outputTable, Row.class).print();

        env.execute();
    }
}
